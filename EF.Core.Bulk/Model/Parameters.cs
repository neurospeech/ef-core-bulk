using Microsoft.EntityFrameworkCore.Query.Internal;
using System;
using System.Collections.Generic;
using System.Linq;

namespace EF.Core.Bulk
{
    public class Parameters : IParameterValues
    {
        private Dictionary<string, object> d = new Dictionary<string, object>();
        public IReadOnlyDictionary<string, object> ParameterValues => d;

        public void AddParameter(string name, object value)
        {
            d.Add(name, value);
        }

        public object RemoveParameter(string name)
        {
            if (d.Remove(name, out object v))
                return v;
            return null;
        }

        public void SetParameter(string name, object value)
        {
            d[name] = value;
        }
    }
}
